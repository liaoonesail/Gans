package com.lanzhou.entity;

public class UserTweprize {
	private int id;
	private int prizeLevel;//中奖等级
	private String userPhone;//中奖用户的注册电话
	private String time;//中奖时间
	private int status;//用户领取状态 0否1是
	private TwelvePrize twelvePrize;//奖品对象
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPrizeLevel() {
		return prizeLevel;
	}
	public void setPrizeLevel(int prizeLevel) {
		this.prizeLevel = prizeLevel;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public TwelvePrize getTwelvePrize() {
		return twelvePrize;
	}
	public void setTwelvePrize(TwelvePrize twelvePrize) {
		this.twelvePrize = twelvePrize;
	}
	
	public UserTweprize(int id, int prizeLevel, String userPhone, String time,
			int status, TwelvePrize twelvePrize) {
		super();
		this.id = id;
		this.prizeLevel = prizeLevel;
		this.userPhone = userPhone;
		this.time = time;
		this.status = status;
		this.twelvePrize = twelvePrize;
	}
	public UserTweprize(int id, int prizeLevel, String userPhone, String time,
			int status) {
		super();
		this.id = id;
		this.prizeLevel = prizeLevel;
		this.userPhone = userPhone;
		this.time = time;
		this.status = status;
	}
	public UserTweprize() {
		super();
	}
	

}
