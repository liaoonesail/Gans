package com.lanzhou.entity;

public class Mould {
	private int id;
	private String mould_name;//模板名
	private String repertory_address;//始发地
	private String logistics;//物流公司
	private int count_way;//计算方式 1按重量计算 2按件计算
	private double one_kg;//首重1kg价格
	private double over_kg;//超过1kg价格
	private double one_piece;//1件价格
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMould_name() {
		return mould_name;
	}
	public void setMould_name(String mould_name) {
		this.mould_name = mould_name;
	}
	public String getRepertory_address() {
		return repertory_address;
	}
	public void setRepertory_address(String repertory_address) {
		this.repertory_address = repertory_address;
	}
	public String getLogistics() {
		return logistics;
	}
	public void setLogistics(String logistics) {
		this.logistics = logistics;
	}
	public int getCount_way() {
		return count_way;
	}
	public void setCount_way(int count_way) {
		this.count_way = count_way;
	}
	public double getOne_kg() {
		return one_kg;
	}
	public void setOne_kg(double one_kg) {
		this.one_kg = one_kg;
	}
	public double getOver_kg() {
		return over_kg;
	}
	public void setOver_kg(double over_kg) {
		this.over_kg = over_kg;
	}
	public double getOne_piece() {
		return one_piece;
	}
	public void setOne_piece(double one_piece) {
		this.one_piece = one_piece;
	}
	public Mould(int id, String mould_name, String repertory_address,
			String logistics, int count_way, double one_kg, double over_kg,
			double one_piece) {
		super();
		this.id = id;
		this.mould_name = mould_name;
		this.repertory_address = repertory_address;
		this.logistics = logistics;
		this.count_way = count_way;
		this.one_kg = one_kg;
		this.over_kg = over_kg;
		this.one_piece = one_piece;
	}
	public Mould(String mould_name, String repertory_address, String logistics,
			int count_way, double one_kg, double over_kg, double one_piece) {
		super();
		this.mould_name = mould_name;
		this.repertory_address = repertory_address;
		this.logistics = logistics;
		this.count_way = count_way;
		this.one_kg = one_kg;
		this.over_kg = over_kg;
		this.one_piece = one_piece;
	}
	public Mould() {
		super();
	}
	

}
