package com.lanzhou.entity;

public class Address {
	private int id;
	private String address;//省 市 区
	private String details;//街道 姓名 电话
	private int user_id;//用户ID
	private String phone;
	private String user_name;
	private int status;//是否默认 0否 1是
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	
	public Address(int id, String address, String details, int user_id,
			String phone, String user_name, int status) {
		super();
		this.id = id;
		this.address = address;
		this.details = details;
		this.user_id = user_id;
		this.phone = phone;
		this.user_name = user_name;
		this.status = status;
	}
	
	public Address(String address, String details, int user_id, String phone,
			String user_name, int status) {
		super();
		this.address = address;
		this.details = details;
		this.user_id = user_id;
		this.phone = phone;
		this.user_name = user_name;
		this.status = status;
	}
	public Address() {
		super();
	}
	

}
