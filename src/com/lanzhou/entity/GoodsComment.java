package com.lanzhou.entity;

public class GoodsComment {
	private int id;
	private int goods_id;//商品ID
	private int user_id;//用户ID
	private int grade;//评价等级（1~5）
	private String comment;//评论内容
	private String comment_time;//评论时间
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(int goods_id) {
		this.goods_id = goods_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getComment_time() {
		return comment_time;
	}
	public void setComment_time(String comment_time) {
		this.comment_time = comment_time;
	}
	
	public GoodsComment(int id, int goods_id, int user_id, int grade,
			String comment, String comment_time) {
		super();
		this.id = id;
		this.goods_id = goods_id;
		this.user_id = user_id;
		this.grade = grade;
		this.comment = comment;
		this.comment_time = comment_time;
	}
	 
	public GoodsComment(int goods_id, int user_id, int grade, String comment,
			String comment_time) {
		super();
		this.goods_id = goods_id;
		this.user_id = user_id;
		this.grade = grade;
		this.comment = comment;
		this.comment_time = comment_time;
	}
	public GoodsComment() {
		super();
	}
	

}
