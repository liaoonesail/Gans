package com.lanzhou.entity;

public class Longpay {
	private int id;
	private int goods_id;//商品ID
	private String start_time;//开始时间
	private String end_time;//结束时间
	private double real_price;//龙支付价格
	private int label;//标签（无、秒杀、团购、龙支付、专场类型）（0,1,2,3,4）如果label等于4就不是团购和秒杀的活动
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(int goods_id) {
		this.goods_id = goods_id;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public double getReal_price() {
		return real_price;
	}
	public void setReal_price(double real_price) {
		this.real_price = real_price;
	}
	public int getLabel() {
		return label;
	}
	public void setLabel(int label) {
		this.label = label;
	}
	public Longpay(int id, int goods_id, String start_time, String end_time,
			double real_price, int label) {
		super();
		this.id = id;
		this.goods_id = goods_id;
		this.start_time = start_time;
		this.end_time = end_time;
		this.real_price = real_price;
		this.label = label;
	}
	public Longpay(int goods_id, String start_time, String end_time,
			double real_price, int label) {
		super();
		this.goods_id = goods_id;
		this.start_time = start_time;
		this.end_time = end_time;
		this.real_price = real_price;
		this.label = label;
	}
	public Longpay() {
		super();
	}
	

}
