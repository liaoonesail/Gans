package com.lanzhou.entity;

public class Gift {
	private int id;
	private double money;//捐款金额
	private int user_id;//用户ID
	private String time;//捐款时间
	private int area_id;//地区ID
	private int page_parameter_id;//期ID
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getArea_id() {
		return area_id;
	}
	public void setArea_id(int area_id) {
		this.area_id = area_id;
	}
	public int getPage_parameter_id() {
		return page_parameter_id;
	}
	public void setPage_parameter_id(int page_parameter_id) {
		this.page_parameter_id = page_parameter_id;
	}
	
	public Gift(int id, double money, int user_id, String time, int area_id,
			int page_parameter_id) {
		super();
		this.id = id;
		this.money = money;
		this.user_id = user_id;
		this.time = time;
		this.area_id = area_id;
		this.page_parameter_id = page_parameter_id;
	}
	
	public Gift(double money, int user_id, String time, int area_id,
			int page_parameter_id) {
		super();
		this.money = money;
		this.user_id = user_id;
		this.time = time;
		this.area_id = area_id;
		this.page_parameter_id = page_parameter_id;
	}
	public Gift() {
		super();
	}
	

}
