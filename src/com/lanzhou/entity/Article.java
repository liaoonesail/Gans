package com.lanzhou.entity;

public class Article {
	private int id;
	private int type;//文章类型：1活动2精选3推荐4新闻
	private String title;//文章标题
	private String addTime;//添加时间
	private String details;//文章详情
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAddTime() {
		return addTime;
	}
	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public Article(int id, int type, String title, String addTime,
			String details) {
		super();
		this.id = id;
		this.type = type;
		this.title = title;
		this.addTime = addTime;
		this.details = details;
	}
	public Article(int type, String title, String addTime, String details) {
		super();
		this.type = type;
		this.title = title;
		this.addTime = addTime;
		this.details = details;
	}
	public Article() {
		super();
	}
	

}
