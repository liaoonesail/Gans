package com.lanzhou.entity;

public class Activity_details {
	private int id;
	private int activity_id;//活动的id
	private String start_time;//活动的开始时间
	private String end_time;//活动结束时间
	private double real_price;//活动价格
	private int goods_id;//商品ID
	private Goods goods;//商品详情
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getActivity_id() {
		return activity_id;
	}

	public void setActivity_id(int activity_id) {
		this.activity_id = activity_id;
	}

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getEnd_time() {
		return end_time;
	}

	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}

	public double getReal_price() {
		return real_price;
	}

	public void setReal_price(double real_price) {
		this.real_price = real_price;
	}

	public int getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(int goods_id) {
		this.goods_id = goods_id;
	}
	public Goods getGoods() {
		return goods;
	}

	public void setGoods(Goods goods) {
		this.goods = goods;
	}
	
	public Activity_details(int id, int activity_id, String start_time,
			String end_time, double real_price, int goods_id, Goods goods) {
		super();
		this.id = id;
		this.activity_id = activity_id;
		this.start_time = start_time;
		this.end_time = end_time;
		this.real_price = real_price;
		this.goods_id = goods_id;
		this.goods = goods;
	}

	public Activity_details(int id, int activity_id, String start_time,
			String end_time, double real_price, int goods_id) {
		super();
		this.id = id;
		this.activity_id = activity_id;
		this.start_time = start_time;
		this.end_time = end_time;
		this.real_price = real_price;
		this.goods_id = goods_id;
	}

	public Activity_details(int activity_id, String start_time,
			String end_time, double real_price, int goods_id) {
		super();
		this.activity_id = activity_id;
		this.start_time = start_time;
		this.end_time = end_time;
		this.real_price = real_price;
		this.goods_id = goods_id;
	}

	public Activity_details() {
		super();
	}
	

}
