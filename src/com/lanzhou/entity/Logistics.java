package com.lanzhou.entity;
/**
 * 物流
 * @author Administrator
 *
 */
public class Logistics {
	private int id;
	private int order_id;//订单ID
	private String logistics_name;//物流名称
	private String logistics_num;//物流号
	private String time;//发货时间
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getOrder_id() {
		return order_id;
	}
	public void setOrder_id(int order_id) {
		this.order_id = order_id;
	}
	public String getLogistics_name() {
		return logistics_name;
	}
	public void setLogistics_name(String logistics_name) {
		this.logistics_name = logistics_name;
	}
	public String getLogistics_num() {
		return logistics_num;
	}
	public void setLogistics_num(String logistics_num) {
		this.logistics_num = logistics_num;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	public Logistics(int order_id, String logistics_name, String logistics_num,
			String time) {
		super();
		this.order_id = order_id;
		this.logistics_name = logistics_name;
		this.logistics_num = logistics_num;
		this.time = time;
	}
	public Logistics(int id, int order_id, String logistics_name,
			String logistics_num, String time) {
		super();
		this.id = id;
		this.order_id = order_id;
		this.logistics_name = logistics_name;
		this.logistics_num = logistics_num;
		this.time = time;
	}
	public Logistics() {
		super();
	}
	

}
