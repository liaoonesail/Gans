package com.lanzhou.entity;
/**
 * 需验证白名单的商品
 * @author Administrator
 *
 */
public class Whitegoods {
	private int id;
	private int goodsId;//商品Id
	private int nameId;//白名單名稱ID
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}

	public int getNameId() {
		return nameId;
	}

	public void setNameId(int nameId) {
		this.nameId = nameId;
	}

	public Whitegoods(int id, int goodsId, int nameId) {
		this.id = id;
		this.goodsId = goodsId;
		this.nameId = nameId;
	}

	public Whitegoods() {
		super();
	}
	
}
