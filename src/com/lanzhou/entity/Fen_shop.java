package com.lanzhou.entity;

public class Fen_shop {
	private int id;
	private String address;//分店地址
	private String phone;//联系电话
	private int shop_id;//总店ID
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public int getShop_id() {
		return shop_id;
	}

	public void setShop_id(int shop_id) {
		this.shop_id = shop_id;
	}
	
	public Fen_shop(int id, String address, String phone, int shop_id) {
		super();
		this.id = id;
		this.address = address;
		this.phone = phone;
		this.shop_id = shop_id;
	}
	
	public Fen_shop(String address, String phone, int shop_id) {
		super();
		this.address = address;
		this.phone = phone;
		this.shop_id = shop_id;
	}

	public Fen_shop() {
		super();
	}
	
}
